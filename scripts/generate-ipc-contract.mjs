import {readFile, writeFile} from 'node:fs/promises'
import {dirname, resolve} from 'node:path'
import {fileURLToPath} from 'node:url'

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const rustContractPath = resolve(repoRoot, 'src-tauri/src/ipc/contract.rs')
const outputPath = resolve(repoRoot, 'frontend/src/shared/ipc/generated/backendCommands.ts')

const rustContract = await readFile(rustContractPath, 'utf8')
const commandBlock = rustContract.match(/pub\(crate\) const COMMANDS:\s*&\[&str\]\s*=\s*&\[([\s\S]*?)\];/)
if (!commandBlock) throw new Error(`Unable to find COMMANDS in ${rustContractPath}`)

const commands = [...commandBlock[1].matchAll(/"([^"]+)"/g)].map((match) => match[1])
if (commands.length === 0) throw new Error(`No IPC commands found in ${rustContractPath}`)

const content = `// This file is generated by scripts/generate-ipc-contract.mjs. Do not edit manually.\n\nexport const backendCommandNames = ${JSON.stringify(commands, null, 2)} as const\n`
await writeFile(outputPath, content, 'utf8')
console.log(`Generated ${commands.length} IPC command names at ${outputPath}`)
